#include <iostream>

using namespace std;

int main()
{
	// I am declaring a variable age of type int
	int age;
	age = 18;
	cout<<age;
	cout<<"\n";			// \n is used to move the cursor to new line
	cout<<endl;					// you can also use endl manipulator instead of \n
	/* I am declaring
		a variable grade
		of type char
	*/
	char grade = 'A';
	cout<<grade;
	
	
		
	return 0;	
}
